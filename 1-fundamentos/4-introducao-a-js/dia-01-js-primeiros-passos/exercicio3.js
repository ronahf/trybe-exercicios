//Utilize if/else para fazer um programa que retorne o maior de três números. Defina, no começo do programa, três constantes com os valores que serão comparados.

let x = 10;
let y = 10;
let z = 10;

if (x > y && x > z){
    console.log(x);
}
else if (y > x && y > z){
    console.log(y);
}
else if (z >= y & z >= x){
    console.log(z);
}
else{
    console.log(x);
}